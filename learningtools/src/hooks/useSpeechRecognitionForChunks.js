import { useState, useEffect, useCallback, useRef } from 'react';

export const useSpeechRecognitionForChunks = () => {
  const [isListening, setIsListening] = useState(false);
  const [transcript, setTranscript] = useState('');
  const [error, setError] = useState(null);
  const [audioBlob, setAudioBlob] = useState(null);

  const recognitionRef = useRef(null);
  const mediaRecorderRef = useRef(null);
  const audioChunksRef = useRef([]);
  const streamRef = useRef(null);
  const finalTranscriptRef = useRef('');
  const interimTranscriptRef = useRef(''); // ‚úÖ NOVO: Tamb√©m guarda interim
  const hasStartedRef = useRef(false); // ‚úÖ NOVO: Flag para saber se come√ßou
  const processingTimeoutRef = useRef(null); // ‚úÖ NOVO: Timeout para processing
  const silenceTimerRef = useRef(null);

  useEffect(() => {
    if (!('webkitSpeechRecognition' in window) && !('SpeechRecognition' in window)) {
      console.error('Speech recognition not supported');
      setError('Speech recognition not supported in your browser');
      return;
    }

    const SpeechRecognition = window.SpeechRecognition || window.webkitSpeechRecognition;
    recognitionRef.current = new SpeechRecognition();

    // ‚úÖ CONFIGURA√á√ÉO OTIMIZADA PARA MOBILE
    recognitionRef.current.continuous = true; // ‚úÖ MUDAN√áA CR√çTICA: false para mobile
    recognitionRef.current.interimResults = true;
    recognitionRef.current.lang = 'en-US';
    recognitionRef.current.maxAlternatives = 1;

    recognitionRef.current.onstart = () => {
      console.log('üéôÔ∏è Recognition started');
      hasStartedRef.current = true;
      setTranscript('üé§ Listening...');
    };

    recognitionRef.current.onresult = (event) => {
      console.log('üìä onresult triggered, results:', event.results.length);

      let interimText = '';
      let finalText = '';

      for (let i = 0; i < event.results.length; i++) {
        const result = event.results[i];
        const text = result[0].transcript;

        console.log(`Result ${i}: "${text}" (final: ${result.isFinal})`);

        if (result.isFinal) {
          if (finalText) {
            finalText += ' ' + text;
          } else {
            finalText = text;
          }
        } else {
          if (interimText) {
            interimText += ' ' + text;
          } else {
            interimText = text;
          }
        }
      }

      // ‚úÖ ATUALIZA REFS
      if (finalText) {
        if (finalTranscriptRef.current) {
          finalTranscriptRef.current += ' ' + finalText;
        } else {
          finalTranscriptRef.current = finalText;
        }
        console.log('‚úÖ Final accumulated:', finalTranscriptRef.current);
      }

      if (interimText) {
        interimTranscriptRef.current = interimText;
      }

      // ‚úÖ ATUALIZA STATE (mostra final ou interim)
      const displayText = finalTranscriptRef.current || interimText;
      if (displayText) {
        setTranscript(displayText);
        console.log('üìù Display text set:', displayText);
      }

      if (silenceTimerRef.current) {
        clearTimeout(silenceTimerRef.current);
      }

      silenceTimerRef.current = setTimeout(() => {
        if (recognitionRef.current && isListening) {
          console.log('üõë Sil√™ncio detectado, parando...');
          recognitionRef.current.stop();
        }
      }, 2000); // Para ap√≥s 2 segundos de sil√™ncio
    };

    recognitionRef.current.onend = () => {
      if (silenceTimerRef.current) {
        clearTimeout(silenceTimerRef.current);
        silenceTimerRef.current = null;
      }
      console.log('üõë Recognition ended');
      console.log('Final transcript ref:', finalTranscriptRef.current);
      console.log('Interim transcript ref:', interimTranscriptRef.current);

      // ‚úÖ PROCESSAMENTO FINAL COM DELAY
      // Espera um pouco para garantir que onresult terminou
      processingTimeoutRef.current = setTimeout(() => {
        const finalText = finalTranscriptRef.current || interimTranscriptRef.current;

        if (finalText && finalText.trim() && finalText !== 'üé§ Listening...') {
          console.log('‚úÖ Setting final transcript:', finalText);
          setTranscript(finalText);
        } else {
          console.warn('‚ö†Ô∏è No transcript captured!');
          setTranscript(''); // Limpa se n√£o h√° texto
        }

        setIsListening(false);

        // Para a grava√ß√£o
        if (mediaRecorderRef.current && mediaRecorderRef.current.state === 'recording') {
          console.log('üé¨ Stopping MediaRecorder from onend');
          mediaRecorderRef.current.stop();
        }
      }, 300); // ‚úÖ 300ms de delay para garantir processamento
    };

    recognitionRef.current.onerror = (event) => {
      console.error('‚ùå Recognition error:', event.error);

      // Limpa timeout se houver
      if (processingTimeoutRef.current) {
        clearTimeout(processingTimeoutRef.current);
      }

      if (event.error === 'no-speech') {
        console.log('No speech detected');
        setError('No speech detected. Please try again.');
        // ‚úÖ Usa transcript interim se houver
        const lastText = interimTranscriptRef.current || finalTranscriptRef.current;
        if (lastText && lastText !== 'üé§ Listening...') {
          setTranscript(lastText);
        }
      } else if (event.error === 'aborted') {
        console.log('Recognition aborted (normal)');
      } else if (event.error === 'audio-capture') {
        setError('Microphone not accessible');
      } else if (event.error === 'not-allowed') {
        setError('Microphone permission denied');
      } else {
        setError(event.error);
      }

      setIsListening(false);

      // Para a grava√ß√£o em caso de erro
      if (mediaRecorderRef.current && mediaRecorderRef.current.state === 'recording') {
        mediaRecorderRef.current.stop();
      }
    };

    return () => {
      if (recognitionRef.current) {
        try {
          recognitionRef.current.stop();
        } catch (e) {
          console.log('Recognition cleanup:', e);
        }
      }

      if (streamRef.current) {
        streamRef.current.getTracks().forEach(track => track.stop());
      }

      if (processingTimeoutRef.current) {
        clearTimeout(processingTimeoutRef.current);
      }
      if (silenceTimerRef.current) {
          clearTimeout(silenceTimerRef.current);
        }
    };
  }, []);

  const startListening = useCallback(async () => {
    console.log('‚ñ∂Ô∏è Start listening called');

    if (recognitionRef.current && !isListening) {
      // ‚úÖ RESET COMPLETO
      setTranscript('');
      setError(null);
      setAudioBlob(null);
      audioChunksRef.current = [];
      finalTranscriptRef.current = '';
      interimTranscriptRef.current = '';
      hasStartedRef.current = false;

      try {
        // Solicita permiss√£o do microfone
        const stream = await navigator.mediaDevices.getUserMedia({
          audio: {
            echoCancellation: true,
            noiseSuppression: true,
            autoGainControl: true,
            sampleRate: 44100
          }
        });

        streamRef.current = stream;

        // ‚úÖ Detecta o melhor MIME type
        let mimeType = 'audio/webm';
        const types = [
          'audio/webm;codecs=opus',
          'audio/webm',
          'audio/ogg;codecs=opus',
          'audio/mp4',
          'audio/wav'
        ];

        for (const type of types) {
          if (MediaRecorder.isTypeSupported(type)) {
            mimeType = type;
            break;
          }
        }

        console.log('üì± Using MIME type:', mimeType);

        mediaRecorderRef.current = new MediaRecorder(stream, { mimeType });

        mediaRecorderRef.current.ondataavailable = (event) => {
          if (event.data.size > 0) {
            audioChunksRef.current.push(event.data);
            console.log('üìº Chunk:', event.data.size, 'bytes');
          }
        };

        mediaRecorderRef.current.onstop = () => {
          console.log('üé¨ Recording stopped');

          if (audioChunksRef.current.length > 0) {
            const blob = new Blob(audioChunksRef.current, { type: mimeType });
            setAudioBlob(blob);
            console.log('‚úÖ Audio blob:', blob.size, 'bytes');
          } else {
            console.warn('‚ö†Ô∏è No audio chunks recorded!');
          }

          // Para o stream
          if (streamRef.current) {
            streamRef.current.getTracks().forEach(track => track.stop());
            streamRef.current = null;
          }
        };

        // ‚úÖ Inicia grava√ß√£o
        mediaRecorderRef.current.start(100);
        console.log('üî¥ Recording started');

        // ‚úÖ Delay antes de iniciar reconhecimento
        await new Promise(resolve => setTimeout(resolve, 200));

        // ‚úÖ Inicia reconhecimento
        console.log('üé§ Starting speech recognition...');
        recognitionRef.current.start();
        setIsListening(true);

        // ‚úÖ FALLBACK: Auto-stop ap√≥s 10 segundos (seguran√ßa)
        setTimeout(() => {
          if (isListening) {
            console.log('‚è±Ô∏è Auto-stopping after 10s');
            stopListening();
          }
        }, 10000);

      } catch (err) {
        console.error('‚ùå Error starting:', err);
        setError(err.message || 'Failed to start recording');

        if (streamRef.current) {
          streamRef.current.getTracks().forEach(track => track.stop());
          streamRef.current = null;
        }
      }
    }
  }, [isListening]);

  const stopListening = useCallback(() => {
    console.log('‚è∏Ô∏è Stop listening called');

    if (processingTimeoutRef.current) {
      clearTimeout(processingTimeoutRef.current);
    }

    if (recognitionRef.current && isListening) {
      try {
        recognitionRef.current.stop();
        console.log('‚úÖ Recognition.stop() called');
      } catch (e) {
        console.log('Stop error:', e);
      }
    }
  }, [isListening]);

  const resetTranscript = useCallback(() => {
    console.log('üßπ Reset');
    setTranscript('');
    setError(null);
    setAudioBlob(null);
    audioChunksRef.current = [];
    finalTranscriptRef.current = '';
    interimTranscriptRef.current = '';
    hasStartedRef.current = false;
  }, []);

  return {
    isListening,
    transcript,
    audioBlob,
    startListening,
    stopListening,
    resetTranscript,
    error
  };
};