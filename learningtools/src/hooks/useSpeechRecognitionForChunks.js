import { useState, useCallback, useRef, useEffect } from 'react';

/**
 * üéØ Speech Recognition com Prote√ß√£o Anti-Feedback
 *
 * SOLU√á√ïES IMPLEMENTADAS:
 * 1. Pausa o reconhecimento quando √°udio do app est√° tocando
 * 2. Delay antes de iniciar reconhecimento ap√≥s √°udio
 * 3. Detec√ß√£o de quando √°udio do sistema est√° tocando
 * 4. Auto-filtragem de repeti√ß√µes
 */
export const useSpeechRecognitionForChunks = (language = 'en-US') => {
  const [isListening, setIsListening] = useState(false);
  const [transcript, setTranscript] = useState('');
  const [interimTranscript, setInterimTranscript] = useState('');
  const [error, setError] = useState(null);
  const [isSupported, setIsSupported] = useState(true);
  const [isAudioPlaying, setIsAudioPlaying] = useState(false);

  const recognitionRef = useRef(null);
  const finalTranscriptRef = useRef('');
  const silenceTimerRef = useRef(null);
  const audioContextRef = useRef(null);
  const playingAudiosRef = useRef(new Set()); // Track de √°udios tocando

  // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
  // INICIALIZA√á√ÉO
  // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
  useEffect(() => {
    if (!('webkitSpeechRecognition' in window) && !('SpeechRecognition' in window)) {
      console.error('‚ùå Speech Recognition n√£o suportado');
      setError('Speech Recognition n√£o √© suportado neste navegador');
      setIsSupported(false);
      return;
    }

    const SpeechRecognition = window.SpeechRecognition || window.webkitSpeechRecognition;
    recognitionRef.current = new SpeechRecognition();

    recognitionRef.current.continuous = true;
    recognitionRef.current.interimResults = true;
    recognitionRef.current.lang = language;
    recognitionRef.current.maxAlternatives = 1;

    recognitionRef.current.onstart = () => {
      console.log('üé§ Escutando...');
      setIsListening(true);
      setError(null);
    };

    recognitionRef.current.onresult = (event) => {
      // ‚ö†Ô∏è N√ÉO processa se √°udio est√° tocando
      if (isAudioPlaying || playingAudiosRef.current.size > 0) {
        console.log('üîá Ignorando resultado (√°udio tocando)');
        return;
      }

      let interim = '';
      let hasFinal = false;

      for (let i = event.resultIndex; i < event.results.length; i++) {
        const text = event.results[i][0].transcript.trim();

        if (event.results[i].isFinal) {
          // ‚úÖ Filtro anti-repeti√ß√£o
          const cleanText = removeDuplicateWords(text);

          if (finalTranscriptRef.current) {
            finalTranscriptRef.current += ' ' + cleanText;
          } else {
            finalTranscriptRef.current = cleanText;
          }
          hasFinal = true;
          console.log('‚úÖ Final (limpo):', cleanText);
        } else {
          interim = text;
        }
      }

      if (hasFinal) {
        setTranscript(finalTranscriptRef.current);
        setInterimTranscript('');
        resetSilenceTimer();
      } else if (interim) {
        setInterimTranscript(interim);
        resetSilenceTimer();
      }
    };

    recognitionRef.current.onend = () => {
      console.log('üõë Recognition terminou');
      setIsListening(false);
      setInterimTranscript('');

      if (finalTranscriptRef.current) {
        setTranscript(finalTranscriptRef.current);
      }

      if (silenceTimerRef.current) {
        clearTimeout(silenceTimerRef.current);
        silenceTimerRef.current = null;
      }
    };

    recognitionRef.current.onerror = (event) => {
      console.error('‚ùå Erro:', event.error);

      if (event.error === 'no-speech') {
        setError('Nenhuma fala detectada. Tente falar mais alto.');
      } else if (event.error === 'not-allowed') {
        setError('üîí Permiss√£o do microfone negada.');
      } else if (event.error === 'audio-capture') {
        setError('‚ùå N√£o foi poss√≠vel acessar o microfone.');
      } else if (event.error === 'aborted') {
        console.log('Recognition aborted (normal)');
      } else {
        setError(`Erro: ${event.error}`);
      }

      setIsListening(false);
    };

    return () => {
      if (recognitionRef.current) {
        try {
          recognitionRef.current.stop();
        } catch (e) {}
      }

      if (silenceTimerRef.current) {
        clearTimeout(silenceTimerRef.current);
      }
    };
  }, [language, isAudioPlaying]);

  // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
  // FILTRO ANTI-REPETI√á√ÉO
  // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
  const removeDuplicateWords = (text) => {
    const words = text.toLowerCase().split(/\s+/);
    const seen = new Map();
    const result = [];
    let lastWord = '';

    for (let i = 0; i < words.length; i++) {
      const word = words[i];

      // Remove repeti√ß√µes consecutivas
      if (word === lastWord) {
        continue;
      }

      // Remove repeti√ß√µes com no m√°ximo 2 palavras de dist√¢ncia
      const lastSeen = seen.get(word);
      if (lastSeen !== undefined && i - lastSeen <= 3) {
        continue;
      }

      result.push(word);
      seen.set(word, i);
      lastWord = word;
    }

    return result.join(' ');
  };

  // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
  // GERENCIAMENTO DE √ÅUDIO DO APP
  // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê

  /**
   * Chame esta fun√ß√£o ANTES de reproduzir qualquer √°udio no app
   */
  const pauseListeningForAudio = useCallback(async (audioElement, duration = null) => {
    console.log('üîá Pausando reconhecimento para √°udio');

    const audioId = Date.now();
    playingAudiosRef.current.add(audioId);
    setIsAudioPlaying(true);

    // Para o reconhecimento temporariamente
    if (recognitionRef.current && isListening) {
      try {
        recognitionRef.current.stop();
      } catch (e) {
        console.log('Stop error:', e);
      }
    }

    // Monitora quando o √°udio terminar
    if (audioElement) {
      const handleEnded = async () => {
        playingAudiosRef.current.delete(audioId);

        if (playingAudiosRef.current.size === 0) {
          setIsAudioPlaying(false);

          // ‚ö†Ô∏è CR√çTICO: Aguarda 500ms antes de reativar reconhecimento
          await new Promise(resolve => setTimeout(resolve, 500));

          // Reinicia reconhecimento se estava ativo
          if (isListening && recognitionRef.current) {
            try {
              recognitionRef.current.start();
              console.log('üé§ Reconhecimento reativado ap√≥s √°udio');
            } catch (e) {
              console.log('Restart error:', e);
            }
          }
        }
      };

      audioElement.addEventListener('ended', handleEnded);
      audioElement.addEventListener('pause', handleEnded);

      // Cleanup
      return () => {
        audioElement.removeEventListener('ended', handleEnded);
        audioElement.removeEventListener('pause', handleEnded);
      };
    } else if (duration) {
      // Se n√£o tem elemento, usa dura√ß√£o manual
      setTimeout(() => {
        playingAudiosRef.current.delete(audioId);

        if (playingAudiosRef.current.size === 0) {
          setIsAudioPlaying(false);
        }
      }, duration);
    }
  }, [isListening]);

  /**
   * Helper para reproduzir √°udio com prote√ß√£o autom√°tica
   */
  const playAudioSafely = useCallback(async (audioSrc) => {
    const audio = new Audio(audioSrc);

    // Pausa reconhecimento
    const cleanup = await pauseListeningForAudio(audio);

    // Reproduz
    await audio.play();

    return { audio, cleanup };
  }, [pauseListeningForAudio]);

  // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
  // AUTO-STOP AP√ìS SIL√äNCIO
  // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
  const resetSilenceTimer = useCallback(() => {
    if (silenceTimerRef.current) {
      clearTimeout(silenceTimerRef.current);
    }

    silenceTimerRef.current = setTimeout(() => {
      console.log('üîá Sil√™ncio detectado');
      if (recognitionRef.current && isListening && !isAudioPlaying) {
        try {
          recognitionRef.current.stop();
        } catch (e) {}
      }
    }, 3000);
  }, [isListening, isAudioPlaying]);

  // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
  // CONTROLES P√öBLICOS
  // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
  const startListening = useCallback(() => {
    if (!isSupported || !recognitionRef.current || isListening) {
      return;
    }

    // ‚ö†Ô∏è N√ÉO inicia se √°udio est√° tocando
    if (isAudioPlaying || playingAudiosRef.current.size > 0) {
      console.warn('‚ö†Ô∏è N√£o pode iniciar: √°udio tocando');
      setError('Aguarde o √°udio terminar antes de falar');
      return;
    }

    console.log('‚ñ∂Ô∏è Iniciando escuta...');

    setTranscript('');
    setInterimTranscript('');
    setError(null);
    finalTranscriptRef.current = '';

    try {
      recognitionRef.current.start();
      resetSilenceTimer();
    } catch (err) {
      console.error('Erro ao iniciar:', err);
      if (!err.message.includes('already started')) {
        setError(err.message);
      }
    }
  }, [isListening, isSupported, isAudioPlaying, resetSilenceTimer]);

  const stopListening = useCallback(() => {
    console.log('‚è∏Ô∏è Parando escuta...');

    if (silenceTimerRef.current) {
      clearTimeout(silenceTimerRef.current);
      silenceTimerRef.current = null;
    }

    if (recognitionRef.current && isListening) {
      try {
        recognitionRef.current.stop();
      } catch (e) {}
    }
  }, [isListening]);

  const toggleListening = useCallback(() => {
    if (isListening) {
      stopListening();
    } else {
      startListening();
    }
  }, [isListening, startListening, stopListening]);

  const reset = useCallback(() => {
    console.log('üßπ Resetando...');
    setTranscript('');
    setInterimTranscript('');
    setError(null);
    finalTranscriptRef.current = '';
  }, []);

  return {
    // Estados
    isListening,
    isSupported,
    isAudioPlaying,
    transcript,
    interimTranscript,
    error,

    // A√ß√µes
    startListening,
    stopListening,
    toggleListening,
    reset,

    // Prote√ß√£o de √°udio
    pauseListeningForAudio,
    playAudioSafely,

    // Computed
    fullTranscript: transcript + (interimTranscript ? ' ' + interimTranscript : '')
  };
};