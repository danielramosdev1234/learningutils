import { useState, useEffect, useCallback, useRef } from 'react';

export const useSpeechRecognitionForChunks = () => {
  const [isListening, setIsListening] = useState(false);
  const [transcript, setTranscript] = useState('');
  const [error, setError] = useState(null);
  const [audioBlob, setAudioBlob] = useState(null);

  const recognitionRef = useRef(null);
  const mediaRecorderRef = useRef(null);
  const audioChunksRef = useRef([]);
  const streamRef = useRef(null);
  const finalTranscriptRef = useRef(''); // ‚úÖ CR√çTICO: Ref para persistir texto final
  const silenceTimerRef = useRef(null); // ‚úÖ Timer para detectar sil√™ncio

  useEffect(() => {
    if (!('webkitSpeechRecognition' in window) && !('SpeechRecognition' in window)) {
      console.error('Speech recognition not supported');
      setError('Speech recognition not supported in your browser');
      return;
    }

    const SpeechRecognition = window.SpeechRecognition || window.webkitSpeechRecognition;
    recognitionRef.current = new SpeechRecognition();

    // CONFIGURA√á√ÉO OTIMIZADA PARA MOBILE
    recognitionRef.current.continuous = true;
    recognitionRef.current.interimResults = true;
    recognitionRef.current.lang = 'en-US';
    recognitionRef.current.maxAlternatives = 1;

    recognitionRef.current.onresult = (event) => {
      let interimText = '';
      let hasFinal = false;

      for (let i = event.resultIndex; i < event.results.length; i++) {
        const text = event.results[i][0].transcript;

        if (event.results[i].isFinal) {
          // ‚úÖ Acumula texto final em ref (persiste entre renders)
          if (finalTranscriptRef.current) {
            finalTranscriptRef.current += ' ' + text;
          } else {
            finalTranscriptRef.current = text;
          }
          hasFinal = true;
          console.log('üé§ Final text captured:', finalTranscriptRef.current);
        } else {
          interimText = text;
          console.log('‚è≥ Interim:', text);
        }
      }

      // Atualiza state com texto final ou interim
      if (hasFinal) {
        setTranscript(finalTranscriptRef.current);
        // ‚úÖ Reset timer de sil√™ncio quando h√° fala final
        resetSilenceTimer();
      } else if (interimText) {
        setTranscript(interimText);
        // ‚úÖ Reset timer quando h√° fala interim
        resetSilenceTimer();
      }
    };

    recognitionRef.current.onend = () => {
      console.log('üõë Recognition ended');
      setIsListening(false);

      // Para a grava√ß√£o quando o reconhecimento terminar
      if (mediaRecorderRef.current && mediaRecorderRef.current.state === 'recording') {
        mediaRecorderRef.current.stop();
      }

      // ‚úÖ Garante que transcript final est√° no state
      if (finalTranscriptRef.current && finalTranscriptRef.current.trim()) {
        setTranscript(finalTranscriptRef.current);
        console.log('‚úÖ Final transcript set:', finalTranscriptRef.current);
      }

      // Limpa timer
      if (silenceTimerRef.current) {
        clearTimeout(silenceTimerRef.current);
        silenceTimerRef.current = null;
      }
    };

    recognitionRef.current.onerror = (event) => {
      console.error('‚ùå Recognition error:', event.error);

      if (event.error === 'no-speech') {
        // N√£o √© um erro cr√≠tico no mobile
        console.log('No speech detected (normal on mobile)');
      } else if (event.error === 'aborted') {
        console.log('Recognition aborted (normal)');
      } else if (event.error === 'audio-capture') {
        setError('Microphone not accessible');
      } else if (event.error === 'not-allowed') {
        setError('Microphone permission denied');
      } else {
        setError(event.error);
      }

      setIsListening(false);

      // Para a grava√ß√£o em caso de erro
      if (mediaRecorderRef.current && mediaRecorderRef.current.state === 'recording') {
        mediaRecorderRef.current.stop();
      }
    };


    return () => {
      if (recognitionRef.current) {
        try {
          recognitionRef.current.stop();
        } catch (e) {
          console.log('Recognition cleanup:', e);
        }
      }

      if (streamRef.current) {
        streamRef.current.getTracks().forEach(track => track.stop());
      }

      if (silenceTimerRef.current) {
        clearTimeout(silenceTimerRef.current);
      }
    };
  }, []);

  // ‚úÖ NOVO: Fun√ß√£o para auto-stop ap√≥s sil√™ncio (importante para mobile)
  const resetSilenceTimer = useCallback(() => {
    if (silenceTimerRef.current) {
      clearTimeout(silenceTimerRef.current);
    }

    // Auto-stop ap√≥s 2 segundos de sil√™ncio
    silenceTimerRef.current = setTimeout(() => {
      console.log('‚è±Ô∏è Silence detected, auto-stopping...');
      if (recognitionRef.current && isListening) {
        try {
          recognitionRef.current.stop();
        } catch (e) {
          console.log('Auto-stop error:', e);
        }
      }
    }, 2000);
  }, [isListening]);

  const startListening = useCallback(async () => {
    console.log('‚ñ∂Ô∏è Start listening called');

    if (recognitionRef.current && !isListening) {
      setTranscript('');
      setError(null);
      setAudioBlob(null);
      audioChunksRef.current = [];
      finalTranscriptRef.current = ''; // ‚úÖ Reset ref

      try {
        // Solicita permiss√£o do microfone
        const stream = await navigator.mediaDevices.getUserMedia({
          audio: {
            echoCancellation: true,
            noiseSuppression: true,
            autoGainControl: true, // ‚úÖ Importante para mobile
            sampleRate: 44100
          }
        });

        const isMobile = (
            /Android/i.test(navigator.userAgent) ||
            /iPhone/i.test(navigator.userAgent) ||
            /iPad/i.test(navigator.userAgent) ||
            (navigator.maxTouchPoints && navigator.maxTouchPoints > 2) ||
            window.innerWidth <= 768
          );

          const isAndroid = /Android/i.test(navigator.userAgent);

          console.log('üì± Is Mobile?', isMobile);
          console.log('ü§ñ Is Android?', isAndroid);
          console.log('üëÜ Touch Points:', navigator.maxTouchPoints);
          console.log('üìè Window Width:', window.innerWidth);



          streamRef.current = stream;

          // ‚úÖ SE FOR MOBILE, S√ì USA SPEECH RECOGNITION (SEM GRAVAR)
          if (isMobile || isAndroid) {
            console.log('üö´ Mobile device detected - Skipping MediaRecorder');

            // ‚ö†Ô∏è CRITICAL: Delay para garantir que o microfone est√° pronto
            await new Promise(resolve => setTimeout(resolve, 300));

            console.log('üé§ Starting SpeechRecognition for mobile...');

            // Inicia o reconhecimento
            recognitionRef.current.start();
            setIsListening(true);
            resetSilenceTimer();

            console.log('‚úÖ SpeechRecognition started successfully');

            return; // ‚ö†Ô∏è SAI AQUI - N√£o cria MediaRecorder
          }

          // üëá DAQUI PRA BAIXO S√ì EXECUTA NO DESKTOP
          console.log('üíª Desktop detected - Using MediaRecorder + SpeechRecognition');
        streamRef.current = stream;

        // Detecta o tipo MIME suportado
        let mimeType = 'audio/webm';

        if (MediaRecorder.isTypeSupported('audio/webm;codecs=opus')) {
          mimeType = 'audio/webm;codecs=opus';
        } else if (MediaRecorder.isTypeSupported('audio/webm')) {
          mimeType = 'audio/webm';
        } else if (MediaRecorder.isTypeSupported('audio/mp4')) {
          mimeType = 'audio/mp4';
        } else if (MediaRecorder.isTypeSupported('audio/ogg;codecs=opus')) {
          mimeType = 'audio/ogg;codecs=opus';
        }

        console.log('üì± Using MIME type:', mimeType);

        mediaRecorderRef.current = new MediaRecorder(stream, {
          mimeType: mimeType
        });

        mediaRecorderRef.current.ondataavailable = (event) => {
          if (event.data.size > 0) {
            audioChunksRef.current.push(event.data);
            console.log('üìº Audio chunk recorded:', event.data.size, 'bytes');
          }
        };

        mediaRecorderRef.current.onstop = () => {
          console.log('üé¨ Recording stopped, creating blob...');
          const blob = new Blob(audioChunksRef.current, { type: mimeType });
          setAudioBlob(blob);
          console.log('‚úÖ Audio blob created:', blob.size, 'bytes');

          // Para o stream do microfone
          if (streamRef.current) {
            streamRef.current.getTracks().forEach(track => track.stop());
            streamRef.current = null;
          }
        };

        // ‚úÖ Inicia grava√ß√£o com chunks pequenos (melhor para mobile)
        mediaRecorderRef.current.start(100); // 100ms chunks
        console.log('üî¥ Recording started');

        // ‚úÖ Delay maior para garantir que MediaRecorder est√° pronto
        await new Promise(resolve => setTimeout(resolve, 200));

        // Inicia o reconhecimento de voz
        recognitionRef.current.start();
        setIsListening(true);

        // ‚úÖ Inicia timer de sil√™ncio
        resetSilenceTimer();

      } catch (err) {
        console.error('Error starting recording:', err);
        setError(err.message || 'Failed to start recording');

        if (streamRef.current) {
          streamRef.current.getTracks().forEach(track => track.stop());
          streamRef.current = null;
        }
      }
    }
  }, [isListening, resetSilenceTimer]);

  const stopListening = useCallback(() => {
    console.log('‚è∏Ô∏è Stop listening called');

    // Limpa timer de sil√™ncio
    if (silenceTimerRef.current) {
      clearTimeout(silenceTimerRef.current);
      silenceTimerRef.current = null;
    }

    if (recognitionRef.current && isListening) {
      try {
        recognitionRef.current.stop();
      } catch (e) {
        console.log('Stop error (ignorable):', e);
      }
    }
  }, [isListening]);

  const resetTranscript = useCallback(() => {
    console.log('üßπ Resetting transcript and audio');
    setTranscript('');
    setError(null);
    setAudioBlob(null);
    audioChunksRef.current = [];
    finalTranscriptRef.current = ''; // ‚úÖ Reset ref
  }, []);

  return {
    isListening,
    transcript,
    audioBlob,
    startListening,
    stopListening,
    resetTranscript,
    error
  };
};